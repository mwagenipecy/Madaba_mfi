    <script>
        // Client Type Distribution Chart
        const clientTypeCtx = document.getElementById('clientTypeChart').getContext('2d');
        
        const individualCount = {{ $clients->where('client_type', 'individual')->count() }};
        const businessCount = {{ $clients->where('client_type', 'business')->count() }};
        
        new Chart(clientTypeCtx, {
            type: 'doughnut',
            data: {
                labels: ['Individual', 'Business'],
                datasets: [{
                    data: [individualCount, businessCount],
                    backgroundColor: [
                        'rgb(59, 130, 246)',
                        'rgb(34, 197, 94)'
                    ]
                }]
            },
            options: {
                responsive: true,
                maintainAspectRatio: false,
                plugins: {
                    legend: {
                        position: 'bottom'
                    },
                    tooltip: {
                        callbacks: {
                            label: function(context) {
                                const value = context.parsed;
                                const total = context.dataset.data.reduce((a, b) => a + b, 0);
                                const percentage = total > 0 ? ((value / total) * 100).toFixed(1) : 0;
                                return context.label + ': ' + value + ' (' + percentage + '%)';
                            }
                        }
                    }
                }
            }
        });
    </script>
